def read_runtime_config():
    api_key = get_secret_or_env(("api_key",), API_KEY_ENV_KEYS)
    configured_model = get_secret_or_env(MODEL_SECRET_KEYS, MODEL_ENV_KEYS)
    model = normalize_gemini_model_name(configured_model)
    if model:
        st.session_state["model_name"] = model

    missing = []
    if not api_key:
        missing.append("api_key/GOOGLE_API_KEY")
    if not configured_model:
        missing.append("default_model/model/DEFAULT_MODEL")
    if missing:
        return {"ready": False, "api_key": "", "model": "", "error": tr("missing_secrets", items=", ".join(missing))}
    return {"ready": True, "api_key": api_key, "model": model, "error": ""}


def normalize_gemini_model_name(model):
    value = (model or "").strip()
    if not value:
        return ""
    value = value.split("?", 1)[0].split(":generateContent", 1)[0].strip("/")
    if value.startswith(GEMINI_API_BASE_URL):
        value = value.removeprefix(GEMINI_API_BASE_URL).strip("/")
    if value.startswith("models/"):
        value = value.removeprefix("models/")
    return value


def parse_ingredients(text):
    parts = re.split(r"[,，、\n]+", text or "")
    return normalize_ingredients(parts)


def catalog_prompt():
    suffix = lang()
    rows = []
    for item in CATALOG:
        rows.append(
            f"- {item['catalog_product_id']} / {item[f'name_{suffix}']} / {item[f'category_{suffix}']} / "
            f"{item[f'promotion_label_{suffix}']} / ${item['price']:.2f} / {item[f'reason_{suffix}']}"
        )
    return "\n".join(rows)


def catalog_item_for_product_name(name):
    product_name = normalize_ingredient_name(name, display=False)
    for item in CATALOG:
        names = (
            normalize_ingredient_name(item.get("name_en", ""), display=False),
            normalize_ingredient_name(item.get("name_zh", ""), display=False),
        )
        if product_name in names:
            return item
    return None


def enrich_recommended_product(product):
    product = dict(product or {})
    catalog_item = catalog_item_for_product_name(product.get("name", ""))
    if catalog_item:
        product.setdefault("catalog_product_id", catalog_item["catalog_product_id"])
        product.setdefault("estimated_price", catalog_item["price"])
        product.setdefault("category", catalog_item[f"category_{lang()}"])
        product.setdefault("promotion_label", catalog_item[f"promotion_label_{lang()}"])
    return product


def clean_response(raw):
    raw = re.sub(r"<thought>.*?</thought>", "", raw or "", flags=re.DOTALL | re.IGNORECASE)
    raw = re.sub(r"```[a-zA-Z]*\n?|```", "", raw)
    return raw.strip()


def extract_json(raw):
    cleaned = clean_response(raw)
    match = re.search(r"(\{.*\})", cleaned, re.DOTALL)
    if not match:
        raise ValueError("No JSON object found.")
    return json.loads(match.group(1))


def call_gemini(api_key, model, user_parts, system_prompt):
    model_path = urllib.parse.quote(normalize_gemini_model_name(model), safe="")
    url = f"{GEMINI_API_BASE_URL}/models/{model_path}:generateContent?key={urllib.parse.quote(api_key)}"
    payload = {
        "systemInstruction": {"parts": [{"text": system_prompt}]},
        "contents": [{"role": "user", "parts": user_parts}],
        "generationConfig": {"responseMimeType": "application/json"},
    }
    request = urllib.request.Request(
        url,
        data=json.dumps(payload).encode("utf-8"),
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    data = None
    timeouts = (GEMINI_TIMEOUT_SECONDS, GEMINI_RETRY_TIMEOUT_SECONDS)
    for attempt, timeout_seconds in enumerate(timeouts, start=1):
        try:
            with urllib.request.urlopen(request, timeout=timeout_seconds) as response:
                data = json.loads(response.read().decode("utf-8"))
            break
        except (TimeoutError, socket.timeout) as exc:
            if attempt == len(timeouts):
                raise RuntimeError(
                    f"Gemini request timed out after {timeout_seconds} seconds. "
                    "Freshwise switched to the local demo plan so the meal-to-cart flow can continue."
                ) from exc
            logging.warning("Gemini request timed out after %s seconds; retrying once.", timeout_seconds)
        except urllib.error.HTTPError as exc:
            detail = exc.read().decode("utf-8", errors="replace")
            raise RuntimeError(f"Gemini HTTP {exc.code}: {detail}") from exc
        except urllib.error.URLError as exc:
            reason = exc.reason
            if isinstance(reason, TimeoutError) or isinstance(reason, socket.timeout):
                if attempt == len(timeouts):
                    raise RuntimeError(
                        f"Gemini request timed out after {timeout_seconds} seconds. "
                        "Freshwise switched to the local demo plan so the meal-to-cart flow can continue."
                    ) from exc
                logging.warning("Gemini connection timed out after %s seconds; retrying once.", timeout_seconds)
                continue
            raise RuntimeError(f"Gemini connection failed: {reason}") from exc

    parts = data.get("candidates", [{}])[0].get("content", {}).get("parts", [])
    text = "".join(part.get("text", "") for part in parts if isinstance(part, dict)).strip()
    if not text:
        raise ValueError(f"Gemini response missing text: {json.dumps(data)[:500]}")
    return text


def generate_plan(api_key, model, ingredients, business_goal):
    output_language = "Traditional Chinese" if lang() == "zh" else "English"
    system_prompt = f"""
You are Freshwise, a multimodal meal-to-cart engine for a grocery app.
Create exactly three practical dinner recommendations from the user's ingredients and the retailer promotion catalog.
Recommend missing items only when useful for the recipe.
Output language: {output_language}.
Business goal: {business_goal}

Promotion catalog:
{catalog_prompt()}

Return only valid JSON with this shape:
{{
  "recipes": [
    {{
      "meal_title": "string",
      "reasoning_summary": "string",
      "chef_note": "string",
      "owned_ingredients": ["string"],
      "missing_items": ["string"],
      "recommended_products": [
        {{"catalog_product_id": "string", "name": "string", "category": "string", "promotion_label": "string", "reason": "string", "estimated_price": 0}}
      ],
      "recipe_steps": ["string"],
      "cart_summary": {{"item_count": 0, "estimated_total": 0}}
    }}
  ]
}}
"""
    raw = call_gemini(
        api_key,
        model,
        [{"text": f"Ingredients: {', '.join(ingredients)}"}],
        system_prompt,
    )
    data = extract_json(raw)
    if isinstance(data.get("recipes"), list):
        data["recipes"] = data["recipes"][:3]
    elif not isinstance(data.get("recommended_products"), list):
        raise ValueError("Model JSON missing recipes.")
    return data


def recognize_ingredients(api_key, model, image_bytes):
    output_language = "Traditional Chinese" if lang() == "zh" else "English"
    encoded = base64.b64encode(image_bytes).decode("ascii")
    raw = call_gemini(
        api_key,
        model,
        [
            {"text": "Identify the visible food ingredients."},
            {"inlineData": {"mimeType": "image/jpeg", "data": encoded}},
        ],
        (
            "You identify visible food ingredients from fridge or kitchen photos. "
            f"Return only JSON in {output_language}: "
            '{"ingredients":["ingredient name"]}. Do not include non-food objects.'
        ),
    )
    data = extract_json(raw)
    ingredients = data.get("ingredients", [])
    if not isinstance(ingredients, list):
        raise ValueError("Model JSON missing ingredients.")
    return normalize_ingredients(ingredients)


def fallback_plan(ingredients):
    owned = ingredients or FALLBACK_PLAN[lang()]["owned_ingredients"]
    recipes = []
    for variant in FALLBACK_RECIPE_VARIANTS[lang()]:
        recipe = {**FALLBACK_PLAN[lang()], **variant}
        recipe["owned_ingredients"] = owned
        recipes.append(recipe)
    return {"recipes": recipes}


def recipe_options(plan):
    if not plan:
        return []
    raw = plan
    recipes = raw.get("recipes") if isinstance(raw, dict) else None
    if not isinstance(recipes, list) or not recipes:
        recipes = [raw]
    normalized = []
    base = FALLBACK_PLAN[lang()]
    for recipe in recipes[:3]:
        merged = {**base, **(recipe or {})}
        merged["owned_ingredients"] = merged.get("owned_ingredients") or base["owned_ingredients"]
        merged["missing_items"] = merged.get("missing_items") or base["missing_items"]
        merged["recommended_products"] = merged.get("recommended_products") or base["recommended_products"]
        merged["recommended_products"] = [
            enrich_recommended_product(product) for product in merged["recommended_products"]
        ]
        merged["recipe_steps"] = merged.get("recipe_steps") or base["recipe_steps"]
        merged["chef_note"] = merged.get("chef_note") or merged.get("reasoning_summary") or base.get("chef_note", "")
        normalized.append(merged)
    return normalized


def normalize_plan(plan):
    recipes = recipe_options(plan)
    if not recipes:
        return {}
    selected = min(st.session_state.get("selected_recipe_index", 0), len(recipes) - 1)
    st.session_state["selected_recipe_index"] = selected
    return recipes[selected]


def sync_cart(plan):
    cart = st.session_state.setdefault("cart_quantities", {})
    product_names = [product.get("name", "") for product in plan["recommended_products"]]
    for name in product_names:
        cart.setdefault(name, 1)
    for name in list(cart):
        if name not in product_names:
            del cart[name]


def cart_totals(plan):
    subtotal = 0.0
    count = 0
    quantities = st.session_state.setdefault("cart_quantities", {})
    for product in plan["recommended_products"]:
        name = product.get("name", "")
        qty = int(quantities.get(name, 1))
        subtotal += float(product.get("estimated_price", 0) or 0) * qty
        count += qty
    return round(subtotal, 2), count


def cart_line_items(plan):
    quantities = st.session_state.setdefault("cart_quantities", {})
    items = []
    for product in plan.get("recommended_products", []):
        name = product.get("name", "")
        qty = int(quantities.get(name, 0) or 0)
        if qty <= 0:
            continue
        price = float(product.get("estimated_price", 0) or 0)
        items.append(
            {
                tr("recommended_products"): name,
                tr("qty"): qty,
                tr("subtotal"): money(price * qty),
            }
        )
    return items


