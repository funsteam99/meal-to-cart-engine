def format_model_error(exc):
    detail = str(exc)
    if "timed out" in detail or "read operation timed out" in detail:
        return (
            "Gemini took too long to respond. Freshwise kept the demo moving with a local recipe plan; "
            "try regenerating in a moment."
        )
    if "unexpected model name format" in detail or "GenerateContentRequest.model" in detail:
        return (
            "Gemini model name format is invalid. Use a model id such as "
            "gemini-2.5-flash or models/gemini-2.5-flash; Freshwise will normalize it before calling Gemini."
        )
    if "SERVICE_DISABLED" in detail or "Gemini API has not been used" in detail:
        return (
            "Gemini API is disabled for this Google Cloud project. Enable it here: "
            f"{GEMINI_API_ENABLE_URL}. If you just enabled it, wait a few minutes and retry."
        )
    if "API_KEY_SERVICE_BLOCKED" in detail or "Requests to this API" in detail:
        return (
            "This API key is blocking Gemini requests. In Google Cloud Console, open the "
            "API key restrictions and allow Generative Language API "
            "(generativelanguage.googleapis.com), or temporarily set API restrictions to "
            "unrestricted while testing."
        )
    if "PERMISSION_DENIED" in detail or "Error code: 403" in detail:
        return (
            "Gemini returned 403 PERMISSION_DENIED. Check that the API key belongs to a "
            "project with Gemini API enabled and that the key is allowed to call it."
        )
    return detail


