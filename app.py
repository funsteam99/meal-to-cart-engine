@st.cache_resource(show_spinner=False)
def bigquery_event_table(project_id, dataset_id, table_id):
    from google.cloud import bigquery

    client = bigquery.Client(project=project_id or None)
    project = project_id or client.project
    dataset_ref = bigquery.Dataset(f"{project}.{dataset_id}")
    dataset_ref.location = "asia-east1"
    client.create_dataset(dataset_ref, exists_ok=True)

    full_table_id = f"{project}.{dataset_id}.{table_id}"
    schema = [
        bigquery.SchemaField("event_id", "STRING", mode="REQUIRED"),
        bigquery.SchemaField("event_timestamp", "TIMESTAMP", mode="REQUIRED"),
        bigquery.SchemaField("event_name", "STRING", mode="REQUIRED"),
        bigquery.SchemaField("session_id", "STRING"),
        bigquery.SchemaField("language", "STRING"),
        bigquery.SchemaField("business_goal", "STRING"),
        bigquery.SchemaField("ingredients", "STRING", mode="REPEATED"),
        bigquery.SchemaField("selected_recipe_index", "INTEGER"),
        bigquery.SchemaField("meal_title", "STRING"),
        bigquery.SchemaField("cart_item_count", "INTEGER"),
        bigquery.SchemaField("cart_subtotal", "FLOAT"),
        bigquery.SchemaField("properties_json", "STRING"),
    ]
    table = bigquery.Table(full_table_id, schema=schema)
    client.create_table(table, exists_ok=True)
    return client, full_table_id


def event_plan_snapshot(plan):
    if not plan:
        return {}
    subtotal, count = cart_totals(plan) if plan.get("recommended_products") else (0, 0)
    return {
        "meal_title": plan.get("meal_title", ""),
        "cart_item_count": count,
        "cart_subtotal": subtotal,
    }


def track_event(event_name, properties=None, plan=None):
    properties = {**read_attribution_config(), **(properties or {})}
    event_config = read_event_config()
    selected_plan = plan or normalize_plan(st.session_state.get("plan"))
    row = {
        "event_id": uuid.uuid4().hex,
        "event_timestamp": datetime.now(timezone.utc).isoformat(),
        "event_name": event_name,
        "session_id": ensure_session_id(),
        "language": lang(),
        "business_goal": st.session_state.get("business_goal", ""),
        "ingredients": normalize_ingredients(st.session_state.get("ingredients", [])),
        "selected_recipe_index": int(st.session_state.get("selected_recipe_index", 0)),
        "properties_json": json.dumps(properties, ensure_ascii=False, sort_keys=True),
        **event_plan_snapshot(selected_plan),
    }
    logging.info("freshwise_event %s", json.dumps(row, ensure_ascii=False, sort_keys=True))
    if not event_config["enabled"]:
        return
    try:
        client, table_id = bigquery_event_table(
            event_config["project_id"],
            event_config["dataset"],
            event_config["table"],
        )
        errors = client.insert_rows_json(table_id, [row])
        if errors:
            logging.error("BigQuery event insert failed: %s", errors)
    except Exception:
        logging.exception("BigQuery event logging failed")


def track_recommended_products(source):
    plan = normalize_plan(st.session_state.get("plan"))
    for index, product in enumerate(plan.get("recommended_products", [])):
        track_event(
            "product_recommended",
            {
                "source": source,
                "product_index": index,
                "catalog_product_id": product.get("catalog_product_id", ""),
                "product_name": product.get("name", ""),
                "category": product.get("category", ""),
                "promotion_label": product.get("promotion_label", ""),
                "estimated_price": float(product.get("estimated_price", 0) or 0),
            },
            plan,
        )


def product_event_properties(product, quantity, previous_quantity=None):
    quantity = int(quantity or 0)
    price = float(product.get("estimated_price", 0) or 0)
    properties = {
        "catalog_product_id": product.get("catalog_product_id", ""),
        "product_name": product.get("name", ""),
        "category": product.get("category", ""),
        "promotion_label": product.get("promotion_label", ""),
        "quantity": quantity,
        "estimated_price": price,
        "line_subtotal": round(price * quantity, 2),
        "reason": product.get("reason", ""),
    }
    if previous_quantity is not None:
        previous_quantity = int(previous_quantity or 0)
        properties["previous_quantity"] = previous_quantity
        properties["quantity_delta"] = quantity - previous_quantity
    return properties


def track_product_quantity_change(product, previous_quantity, quantity, plan):
    event_name = "product_added_to_cart" if quantity > previous_quantity else "product_removed_from_cart"
    track_event(event_name, product_event_properties(product, quantity, previous_quantity), plan)
    track_event(
        "cart_quantity_changed",
        product_event_properties(product, quantity, previous_quantity),
        plan,
    )


def track_order_line_items(plan, order_id):
    quantities = st.session_state.setdefault("cart_quantities", {})
    for product in plan.get("recommended_products", []):
        name = product.get("name", "")
        quantity = int(quantities.get(name, 0) or 0)
        if quantity <= 0:
            continue
        track_event(
            "order_line_item",
            {**product_event_properties(product, quantity), "order_id": order_id},
            plan,
        )


def row_to_dict(row):
    return {key: row[key] for key in row.keys()}


@st.cache_data(ttl=60, show_spinner=False)
def fetch_admin_dashboard(project_id, dataset_id, table_id):
    client, full_table_id = bigquery_event_table(project_id, dataset_id, table_id)
    table_ref = f"`{full_table_id}`"
    window_filter = "event_timestamp >= TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL 30 DAY)"

    metrics_sql = f"""
        SELECT
          COUNT(1) AS event_count,
          COUNT(DISTINCT session_id) AS session_count,
          COUNTIF(event_name = 'recipe_generated') AS recipe_count,
          COUNTIF(event_name = 'product_recommended') AS product_recommendation_count,
          COUNTIF(event_name = 'checkout_started') AS checkout_count,
          COUNTIF(event_name = 'order_completed') AS order_count,
          COUNTIF(event_name = 'order_line_item') AS order_line_item_count,
          AVG(IF(event_name = 'order_completed', cart_subtotal, NULL)) AS avg_order_cart_subtotal,
          SUM(
            IF(
              event_name = 'order_line_item',
              CAST(JSON_VALUE(properties_json, '$.line_subtotal') AS FLOAT64),
              0
            )
          ) AS product_mock_gmv
        FROM {table_ref}
        WHERE {window_filter}
    """
    top_ingredients_sql = f"""
        WITH normalized_ingredients AS (
          SELECT
            CASE
              WHEN REGEXP_CONTAINS(TRIM(ingredient), r'[A-Za-z]') THEN INITCAP(LOWER(TRIM(ingredient)))
              ELSE TRIM(ingredient)
            END AS normalized_ingredient
          FROM {table_ref}, UNNEST(ingredients) AS ingredient
          WHERE {window_filter}
            AND TRIM(ingredient) != ''
        )
        SELECT normalized_ingredient AS ingredient, COUNT(1) AS event_count
        FROM normalized_ingredients
        GROUP BY normalized_ingredient
        ORDER BY event_count DESC, ingredient
        LIMIT 10
    """
    top_products_sql = f"""
        SELECT
          JSON_VALUE(properties_json, '$.product_name') AS product_name,
          COUNT(1) AS exposure_count
        FROM {table_ref}
        WHERE {window_filter}
          AND event_name = 'product_recommended'
          AND JSON_VALUE(properties_json, '$.product_name') IS NOT NULL
        GROUP BY product_name
        ORDER BY exposure_count DESC, product_name
        LIMIT 10
    """
    ordered_products_sql = f"""
        SELECT
          JSON_VALUE(properties_json, '$.product_name') AS product_name,
          SUM(CAST(JSON_VALUE(properties_json, '$.quantity') AS INT64)) AS ordered_units,
          SUM(CAST(JSON_VALUE(properties_json, '$.line_subtotal') AS FLOAT64)) AS product_mock_gmv
        FROM {table_ref}
        WHERE {window_filter}
          AND event_name = 'order_line_item'
          AND JSON_VALUE(properties_json, '$.product_name') IS NOT NULL
        GROUP BY product_name
        ORDER BY product_mock_gmv DESC, ordered_units DESC, product_name
        LIMIT 10
    """
    promotion_performance_sql = f"""
        WITH session_events AS (
          SELECT
            session_id,
            JSON_VALUE(properties_json, '$.retailer_id') AS retailer_id,
            JSON_VALUE(properties_json, '$.promotion_id') AS promotion_id,
            MAX(IF(event_name = 'recipe_generated', 1, 0)) AS has_recipe,
            MAX(IF(event_name = 'checkout_started', 1, 0)) AS has_checkout,
            MAX(IF(event_name = 'order_completed', 1, 0)) AS has_order,
            MAX(IF(event_name = 'order_completed', cart_subtotal, NULL)) AS mock_order_value
          FROM {table_ref}
          WHERE {window_filter}
          GROUP BY session_id, retailer_id, promotion_id
        )
        SELECT
          retailer_id,
          promotion_id,
          COUNT(*) AS sessions,
          SUM(has_recipe) AS recipe_sessions,
          SUM(has_checkout) AS checkout_sessions,
          SUM(has_order) AS order_sessions,
          SAFE_DIVIDE(SUM(has_checkout), SUM(has_recipe)) AS recipe_to_checkout_rate,
          SUM(IFNULL(mock_order_value, 0)) AS mock_gmv
        FROM session_events
        GROUP BY retailer_id, promotion_id
        ORDER BY mock_gmv DESC, order_sessions DESC
        LIMIT 10
    """
    model_performance_sql = f"""
        SELECT
          JSON_VALUE(properties_json, '$.model_name') AS model_name,
          COUNTIF(event_name = 'recipe_generated') AS recipes_generated,
          COUNTIF(event_name = 'ingredient_detected') AS photo_recognitions,
          COUNTIF(event_name = 'order_completed') AS mock_orders,
          AVG(IF(event_name = 'order_completed', cart_subtotal, NULL)) AS average_mock_order_value
        FROM {table_ref}
        WHERE {window_filter}
        GROUP BY model_name
        ORDER BY recipes_generated DESC, mock_orders DESC
        LIMIT 10
    """
    recent_events_sql = f"""
        SELECT
          event_timestamp,
          event_name,
          session_id,
          meal_title,
          cart_item_count,
          cart_subtotal,
          properties_json
        FROM {table_ref}
        WHERE {window_filter}
        ORDER BY event_timestamp DESC
        LIMIT 25
    """

    metrics_rows = list(client.query(metrics_sql).result())
    metrics = row_to_dict(metrics_rows[0]) if metrics_rows else {}
    top_ingredients = [row_to_dict(row) for row in client.query(top_ingredients_sql).result()]
    top_products = [row_to_dict(row) for row in client.query(top_products_sql).result()]
    ordered_products = [row_to_dict(row) for row in client.query(ordered_products_sql).result()]
    promotion_performance = [row_to_dict(row) for row in client.query(promotion_performance_sql).result()]
    model_performance = [row_to_dict(row) for row in client.query(model_performance_sql).result()]
    recent_events = [row_to_dict(row) for row in client.query(recent_events_sql).result()]
    return {
        "metrics": metrics,
        "top_ingredients": top_ingredients,
        "top_products": top_products,
        "ordered_products": ordered_products,
        "promotion_performance": promotion_performance,
        "model_performance": model_performance,
        "recent_events": recent_events,
    }


